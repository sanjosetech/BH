const langMeta = {
  en:    { label: "English",  dir: "ltr", flag: "🇺🇸" },
  es:    { label: "Español",  dir: "ltr", flag: "🇲🇽" },
  fr:    { label: "Français", dir: "ltr", flag: "🇫🇷" },
  ar:    { label: "العربية",  dir: "rtl", flag: "🇸🇦" },
  "pt-BR": { label: "Português", dir: "ltr", flag: "🇧🇷" },
  zh:    { label: "中文",      dir: "ltr", flag: "🇨🇳" },
};

function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;

  // Apply text to all [data-i18n] elements
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const val = key.split(".").reduce((o, k) => o?.[k], t);
    if (val !== undefined) el.textContent = val;
  });

  // Apply dir attribute for RTL
  document.documentElement.dir = langMeta[lang]?.dir || "ltr";
  document.documentElement.lang = lang;

  // Persist preference
  localStorage.setItem("binghill-lang", lang);

  // Update switcher active state
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });
}

function buildLangSwitcher() {
  // Find or create the switcher container in the nav
  let container = document.getElementById("lang-switcher");
  if (!container) {
    container = document.createElement("div");
    container.id = "lang-switcher";
    container.className = "lang-switcher";
    const nav = document.getElementById("navbar");
    if (nav) nav.appendChild(container);
  }

  // Build button for each lang
  container.innerHTML = "";
  Object.entries(langMeta).forEach(([code, meta]) => {
    const btn = document.createElement("button");
    btn.className = "lang-btn";
    btn.setAttribute("data-lang", code);
    btn.setAttribute("aria-label", meta.label);
    btn.textContent = meta.label;
    btn.addEventListener("click", () => applyTranslations(code));
    container.appendChild(btn);
  });
}

function initI18n() {
  const saved = localStorage.getItem("binghill-lang") || "en";
  buildLangSwitcher();
  applyTranslations(saved);
}
